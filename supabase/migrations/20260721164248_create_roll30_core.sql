with check (exists (select 1 from public.sessions s where s.id = session_id and public.is_campaign_gm(s.campaign_id)));

insert into storage.buckets (id, name, public)
values ('campaign-media', 'campaign-media', false)
on conflict (id) do update set public = excluded.public;

create policy "members read campaign media" on storage.objects for select to authenticated
using (
  bucket_id = 'campaign-media'
  and public.is_campaign_member(((storage.foldername(name))[1])::uuid)
);
create policy "gms upload campaign media" on storage.objects for insert to authenticated
with check (
  bucket_id = 'campaign-media'
  and public.is_campaign_gm(((storage.foldername(name))[1])::uuid)
);
create policy "gms update campaign media" on storage.objects for update to authenticated
using (
  bucket_id = 'campaign-media'
  and public.is_campaign_gm(((storage.foldername(name))[1])::uuid)
)
with check (
  bucket_id = 'campaign-media'
  and public.is_campaign_gm(((storage.foldername(name))[1])::uuid)
);
create policy "gms delete campaign media" on storage.objects for delete to authenticated
using (
  bucket_id = 'campaign-media'
  and public.is_campaign_gm(((storage.foldername(name))[1])::uuid)
);

do $$
declare
  table_name text;
begin
  foreach table_name in array array[
    'messages', 'prompt_responses', 'prompts', 'purchase_requests', 'session_events', 'sessions'
  ] loop
    if not exists (
      select 1 from pg_publication_tables
      where pubname = 'supabase_realtime' and schemaname = 'public' and tablename = table_name
    ) then
      execute format('alter publication supabase_realtime add table public.%I', table_name);
    end if;
  end loop;
end;
$$;

