create or replace function public.create_roll30_campaign(campaign_name text)
returns public.campaigns
language plpgsql
security definer
set search_path = public
as $$
declare
  actor uuid := auth.uid();
  result public.campaigns;
begin
  if actor is null then raise exception 'Authentication required'; end if;
  if nullif(btrim(campaign_name), '') is null then raise exception 'Campaign name is required'; end if;
  insert into public.profiles (id, display_name)
  values (
    actor,
    coalesce(
      nullif(auth.jwt() -> 'user_metadata' ->> 'display_name', ''),
      split_part(coalesce(auth.jwt() ->> 'email', 'Adventurer'), '@', 1)
    )
  ) on conflict (id) do nothing;
  insert into public.campaigns (owner_id, name)
  values (actor, btrim(campaign_name)) returning * into result;
  insert into public.campaign_members (campaign_id, user_id, role)
  values (result.id, actor, 'gm');
  return result;
end;
$$;

create or replace function public.join_roll30_campaign(campaign_code text)
returns public.campaigns
language plpgsql
security definer
set search_path = public
as $$
declare
  actor uuid := auth.uid();
  result public.campaigns;
begin
  if actor is null then raise exception 'Authentication required'; end if;
  select * into result from public.campaigns where join_code = upper(btrim(campaign_code));
  if result.id is null then raise exception 'Campaign not found'; end if;
  insert into public.profiles (id, display_name)
  values (
    actor,
    coalesce(
      nullif(auth.jwt() -> 'user_metadata' ->> 'display_name', ''),
      split_part(coalesce(auth.jwt() ->> 'email', 'Adventurer'), '@', 1)
    )
  ) on conflict (id) do nothing;
  insert into public.campaign_members (campaign_id, user_id, role)
  values (result.id, actor, 'player') on conflict (campaign_id, user_id) do nothing;
  return result;
end;
$$;

create or replace function public.change_roll30_hp(target_character uuid, delta integer)
returns public.characters
language plpgsql
security definer
set search_path = public
as $$
declare
  result public.characters;
begin
  select * into result from public.characters where id = target_character for update;
  if result.id is null then raise exception 'Character not found'; end if;
  if not (public.is_campaign_gm(result.campaign_id) or result.owner_id is not distinct from auth.uid()) then
    raise exception 'Not permitted';
  end if;
  update public.characters
  set hp_current = greatest(0, least(coalesce(result.hp_max, 999999), coalesce(result.hp_current, 0) + delta)),
      updated_at = now()
  where id = target_character returning * into result;
  return result;
end;
$$;

create or replace function public.resolve_roll30_purchase(request_id uuid, approve boolean)
returns public.purchase_requests
language plpgsql
security definer
set search_path = public
as $$
declare
  request_row public.purchase_requests;
  shop_row public.shops;
  stock_row public.shop_stock;
  character_row public.characters;
  gp integer;
  total integer;
begin
  select * into request_row from public.purchase_requests where id = request_id for update;
  if request_row.id is null then raise exception 'Purchase request not found'; end if;
  select * into shop_row from public.shops where id = request_row.shop_id;
  if not public.is_campaign_gm(shop_row.campaign_id) then raise exception 'Only the GM can resolve purchases'; end if;
  if request_row.status <> 'pending' then raise exception 'Purchase request is already resolved'; end if;
  if not approve then
    update public.purchase_requests set status = 'declined', resolved_at = now()
    where id = request_id returning * into request_row;
    return request_row;
  end if;
  select * into stock_row from public.shop_stock
  where shop_id = request_row.shop_id and item_id = request_row.item_id for update;
  if stock_row.item_id is null then raise exception 'Item is no longer stocked'; end if;
  if stock_row.quantity is not null and stock_row.quantity < request_row.quantity then raise exception 'Not enough stock'; end if;
  select * into character_row from public.characters where id = request_row.character_id for update;
  gp := coalesce((character_row.sheet -> 'currency' ->> 'gp')::integer, 0);
  total := stock_row.price * request_row.quantity;
  if gp < total then raise exception 'Character cannot afford this purchase'; end if;
  update public.characters
  set sheet = jsonb_set(character_row.sheet, '{currency,gp}', to_jsonb(gp - total), true), updated_at = now()
  where id = character_row.id;
  insert into public.character_inventory (character_id, item_id, quantity)
  values (character_row.id, request_row.item_id, request_row.quantity)
  on conflict (character_id, item_id) do update
  set quantity = public.character_inventory.quantity + excluded.quantity;
  if stock_row.quantity is not null then
    update public.shop_stock set quantity = quantity - request_row.quantity
    where shop_id = stock_row.shop_id and item_id = stock_row.item_id;
  end if;
  update public.purchase_requests set status = 'completed', resolved_at = now()
  where id = request_id returning * into request_row;
  return request_row;
end;
$$;

